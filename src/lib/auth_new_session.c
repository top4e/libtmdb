#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "tmdb.h"

#include "auth_new_session.h"

/*
	AUTHORIZATION / NEW SESSION ID
*/

int tmdb_auth_new_session(char *request_token)
{
	int			result;
	char			*url_str;
	net_data_t		data;
	tmdb_request_ctx_t	ctx;

	init_data_chunk(&data);

	memset(&ctx, 0, sizeof(tmdb_request_ctx_t));
	tmdb_ctx_set(&ctx, API_URL_F, NULL);
	tmdb_ctx_set(&ctx, API_HEADER_F, NULL);
	tmdb_ctx_set(&ctx, API_KEY_F, NULL);
	ctx.request_type.type = AUTH_SESSION_NEW;

	tmdb_ctx_set(&ctx, REQUEST_TOKEN, request_token);

	url_str = tmdb_get_url_from_ctx(&ctx);

	result = nethelper_get(url_str, API_HEADER, &data);

	free(url_str);
	free_ctx(&ctx);

	if (!result) {
		header_process(&data);
		body_process(&data, AUTH_NEW_SESSION_NUM_TOKENS, NULL, NULL);
	}
	free_data_chunk(&data);

	return result;
}
